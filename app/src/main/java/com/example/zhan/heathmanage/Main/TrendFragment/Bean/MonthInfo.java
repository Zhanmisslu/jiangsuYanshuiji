package com.example.zhan.heathmanage.Main.TrendFragment.Bean;

import java.util.List;

public class MonthInfo {
    private String HeartRate;//心率
    private String Date;//时间
    private String DiastolicBP;//舒张压
    private String SystolicBP;//收缩压
    private String BloodFat;//血脂
    private String BloodOxygen;//血氧

    public String getHeartRate() {
        return HeartRate;
    }

    public void setHeartRate(String heartRate) {
        HeartRate = heartRate;
    }

    public String getDate() {
        return Date;
    }

    public void setDate(String date) {
        Date = date;
    }

    public String getDiastolicBP() {
        return DiastolicBP;
    }

    public void setDiastolicBP(String diastolicBP) {
        DiastolicBP = diastolicBP;
    }

    public String getSystolicBP() {
        return SystolicBP;
    }

    public void setSystolicBP(String systolicBP) {
        SystolicBP = systolicBP;
    }

    public String getBloodFat() {
        return BloodFat;
    }

    public void setBloodFat(String bloodFat) {
        BloodFat = bloodFat;
    }

    public String getBloodOxygen() {
        return BloodOxygen;
    }

    public void setBloodOxygen(String bloodOxygen) {
        BloodOxygen = bloodOxygen;
    }
}
